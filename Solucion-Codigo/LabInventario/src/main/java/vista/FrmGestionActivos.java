    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        try {
            if (txtCodigo.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty() || txtCosto.getText().trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Complete los campos obligatorios.");
                return;
            }

            String codigo = txtCodigo.getText().trim();
            String nombre = txtNombre.getText().trim();
            String desc = txtDescripcion.getText().trim();
            double costo = Double.parseDouble(txtCosto.getText().trim());
            java.time.LocalDate fechaAdq = java.time.LocalDate.parse(txtFechaAdq.getText().trim());
            String tipo = cmbTipoActivo.getSelectedItem().toString();

            if ("HARDWARE".equals(tipo)) {
                int freq = Integer.parseInt(txtFrecuenciaMant.getText().trim());
                Modelo.Hardware hw = new Modelo.Hardware(codigo, nombre, desc, costo, fechaAdq, freq);
                activoDAO.crear(hw, "LAB-DEFAULT", usuarioActual.getCodigo());
            } else {
                double numEq = Double.parseDouble(txtNumEquipos.getText().trim());
                java.time.LocalDate fechaRen = java.time.LocalDate.parse(txtFechaRenovacion.getText().trim());
                Modelo.Software sw;

                if ("SUBSCRIPCION".equals(tipo)) {
                    sw = new Modelo.Subscripcion("ANUAL", numEq, fechaRen, codigo, nombre, desc, costo, fechaAdq);
                } else if ("LICENCIA".equals(tipo)) {
                    sw = new Modelo.Licencia("LIC-" + codigo, numEq, fechaRen, codigo, nombre, desc, costo, fechaAdq);
                } else {
                    sw = new Modelo.Software(numEq, fechaRen, codigo, nombre, desc, costo, fechaAdq);
                }
                activoDAO.crear(sw, "LAB-DEFAULT", usuarioActual.getCodigo());
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Activo guardado exitosamente.");
            limpiarFormulario();
            cargarTablaActivos();

        } catch (java.time.format.DateTimeParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use AAAA-MM-DD.");
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese valores numéricos validos en Costo, Frecuencia o Equipos.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

