    private void inicializarSistema() {
        try {
            java.sql.Connection conn = java.sql.DriverManager.getConnection("jdbc:sqlite:lab_inventario.db");
            this.activoDAO = new Controlador.ActivoDAO(conn);

            Controlador.Servicios.ServicioAutorizacion auth = new Controlador.Servicios.ServicioAutorizacion();
            Controlador.Servicios.ServicioHardware servHW = new Controlador.Servicios.ServicioHardware();
            Controlador.Servicios.ServicioSoftware servSW = new Controlador.Servicios.ServicioSoftware();

            this.controladorHW = new Controlador.ControladorHardware(auth, servHW, activoDAO);
            this.controladorSW = new Controlador.ControladorSoftware(auth, servSW, activoDAO);

            cargarTablaActivos();
            aplicarPermisosPorRol();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error base de datos: " + e.getMessage());
        }
    }

    private void cargarTablaActivos() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblActivos.getModel();
        model.setRowCount(0);

        java.util.List<Modelo.Hardware> listaHW = controladorHW.obtenerHardwareVisible(usuarioActual);
        for (Modelo.Hardware hw : listaHW) {
            model.addRow(new Object[]{hw.getCodigo(), hw.getNombre(), hw.getTipoActivo(), hw.getEstado()});
        }

        java.util.List<Modelo.Software> listaSW = controladorSW.obtenerSoftwareVisible(usuarioActual);
        for (Modelo.Software sw : listaSW) {
            model.addRow(new Object[]{sw.getCodigo(), sw.getNombre(), sw.getTipoActivo(), sw.getEstado()});
        }
    }

    private void aplicarPermisosPorRol() {
        if (usuarioActual.getNivelAcceso() == Modelo.NivelAcceso.USUARIO) {
            btnGuardar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnLimpiar.setEnabled(false);
        }
    }

