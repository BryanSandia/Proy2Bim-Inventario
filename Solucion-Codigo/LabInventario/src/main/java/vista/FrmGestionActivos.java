    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tblActivos.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un activo de la tabla para eliminar.");
            return;
        }

        String codigo = tblActivos.getValueAt(fila, 0).toString();
        String tipo = tblActivos.getValueAt(fila, 2).toString();

        int resp = javax.swing.JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar el activo " + codigo + "?");
        if (resp == javax.swing.JOptionPane.YES_OPTION) {
            if ("HARDWARE".equals(tipo)) {
                String res = controladorHW.eliminarActivo(usuarioActual, codigo);
                javax.swing.JOptionPane.showMessageDialog(this, res);
            } else {
                activoDAO.eliminar(codigo);
                javax.swing.JOptionPane.showMessageDialog(this, "Activo eliminado.");
            }
            cargarTablaActivos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

